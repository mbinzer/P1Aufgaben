public class Account {
	
	public float balance;
	public String owner;
	
	public Account(float start, String name) {
		balance = start;
		owner = name;
	}

	public void deposit(float in) {
		balance += in;
	}
	public void drawOut(float out) {
		balance -= out;
	}		
	public float getbalance(){
		return balance;	
	}
	public void print() {
		System.out.println("Kontostand (" + owner + "): " + balance + " Euro \n Auf Wiedersehen");
	}
}
